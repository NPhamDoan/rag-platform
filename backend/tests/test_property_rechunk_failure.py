"""Property-based test for Property 24 (task 8.7): a failed re-chunk keeps the old Chunks intact.

# Feature: multi-user-rag-platform, Property 24: A failed re-chunk / re-embed keeps
# the old Chunks intact — for any uploaded document, if `DocumentPipeline.rechunk`
# fails (for one of the two causes Hypothesis picks:
#   (1) a non-existent strategy name -> ValidationError BEFORE writing to the DB; or
#   (2) an error while committing the replace-set transaction (injected error: monkeypatch db.commit to raise once))
# then rechunk MUST raise, and AFTERWARDS the Chunk records (count, order, id,
# noiDung, boundaries) as well as `taiLieu.trangThai` MUST stay exactly as they were
# before the failed re-chunk (comparing a fingerprint before/after).
# Validates: Requirements 5.6, 18.10

REUSE fakes (FakeEmbeddingProvider / FakeClient / FakeCollection) from
`test_document_pipeline_rechunk` as required. Each example uses a NEW in-memory
SQLite. Text is generated by Hypothesis (mixing several fragments + free text),
filtering out whitespace-only text with `assume`. max_examples=100.
"""

